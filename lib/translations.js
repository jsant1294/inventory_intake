export function t(lang, key) {
  return TEXT[lang]?.[key] || TEXT.en[key] || key;
}

export function formatText(lang, key, vars = {}) {
  let str = t(lang, key);
  for (const [k, v] of Object.entries(vars)) {
    str = str.replaceAll(`{${k}}`, String(v));
  }
  return str;
}
